// Coin change problem

int make_change(int *coins, int size, int money)
{
    sort(coins, coins + size, greater<int>());
    int count = 0, i = 0;

    while (money > 0)
    {
        if (i < size && money >= coins[i])
        {
            money -= coins[i];
            ++count;
        }
        else
        {
            i++;
        }

        if (i == size)
        {
            cout << "No answer";
            return -1; // Return -1 to indicate failure
        }
    }

    return count;
}

void solve()
{
    int n;
    cin >> n;

    int coins[] = {1, 2, 5, 10, 20, 50, 100, 200, 500, 2000};
    int t = sizeof(coins) / sizeof(coins[0]);

    int result = make_change(coins, t, n);
    if (result != -1)
    {
        cout << result;
    }
}
