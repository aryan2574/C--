/*
Given an integer num, return a string of its base 7 representation.

Example 1:
Input: num = 100
Output: "202"

Example 2:
Input: num = -7
Output: "-10"
*/

string converToBase7(int num)
{
    if (num == 0)
        return "0";
    int n = num > 0 ? num : -num;

    string ans = "";
    while (n > 0)
    {
        ans += (n % 7) + '0';
        n = n / 7;
    }
    reverse(ans.begin(), ans.end());
    return num > 0 ? ans : ("-" + ans);
}

string convertToBase7Recursion(int n)
{
    if (n < 0)
        return "-" + convertToBase7Recursion(-n);
    if (n < 7)
        return to_string(n);
    return convertToBase7Recursion(n / 7) + to_string(n % 7);
}

void solve()
{
    int num = -7;

    cout << converToBase7(num) << nline;

    cout << convertToBase7Recursion(num) << nline;
}

