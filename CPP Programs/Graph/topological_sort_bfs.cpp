class Graph
{
    list<int> *l;
    int V;

public:
    Graph(int V)
    {
        this->V = V;
        l = new list<int>[V];
    }

    void addEdge(int x, int y)
    {
        l[x].push_back(y);
    }

    void topological_sort()
    {
        // Create an Indegree array
        int *indegree = new int[V];
        // Initially mark all indegrees as 0
        for (int i = 0; i < V; i++)
        {
            indegree[i] = 0;
        }

        // Update indegree by traversing edges x -> y
        // indegree[y] ++

        for (int i = 0; i < V; i++)
        {
            for (auto y : l[i])
            {
                indegree[y]++;
            }
        }

        // BFS
        queue<int> q;
        // Step 1: Find nodes with 0 indegree
        for (int i = 0; i < V; i++)
        {
            if (indegree[i] == 0)
            {
                q.push(i);
            }
        }

        // Start removing elements from queue
        while (!q.empty())
        {
            int node = q.front();
            cout << node << " ";
            q.pop();

            // Iterate over nbrs of that node and reduce their indegree by 1
            for (auto nbr : l[node])
            {
                indegree[nbr]--;
                if (indegree[nbr] == 0)
                {
                    q.push(nbr);
                }
            }
        }
    }
};

void solve()
{
    Graph g(6);

    g.addEdge(0, 2);
    g.addEdge(1, 2);
    g.addEdge(1, 4);
    g.addEdge(2, 3);
    g.addEdge(2, 5);
    g.addEdge(3, 5);
    g.addEdge(4, 5);

    g.topological_sort();
}

