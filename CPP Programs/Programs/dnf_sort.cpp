// DNF SORT : Three pointer aprroach
// If value of mid = 0 , swap with left and if mid = 2 swap with right. Keep 1 at the middle.

void solve()
{
    int n;
    cin >> n;

    int arr[n];
    for (int i = 0; i < n; i++)
        cin >> arr[i];

    int low = 0, high = n - 1, mid = 0;

    while (mid <= high)
    {
        if (arr[mid] == 0)
        {
            swap(arr[low], arr[mid]);
            low++, mid++;
        }
        else if (arr[mid] == 1)
            mid++;
        else
        {
            swap(arr[high], arr[mid]);
            high--;
        }
    }

    for (auto i : arr)
        cout << i << nline;
}

