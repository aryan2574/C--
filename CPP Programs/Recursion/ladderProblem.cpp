// Function to reach top of a ladder with 'n' steps when
// a person can take 1, 2 ,3 steps at a time.
int countSteps(int n)
{
    // Base Case
    if (n < 0)
        return 0;
    if (n == 0)
        return 1;

    // Recursive Case
    return countSteps(n - 1) + countSteps(n - 2) + countSteps(n - 3);
}

// If 'k' steps can be taken at a time and we want to climb 'n' steps:
int countKSteps(int n, int k)
{
    // Base Case
    if (n < 0)
        return 0;
    if (n == 0)
        return 1;

    int ans(0);
    for (int i = 1; i <= k; i++)
    {
        ans += countKSteps(n - i, k);
    }
    return ans;
}

void solve()
{
    int n, k;
    cin >> n >> k;
    int ans = countKSteps(n, k);
    cout << ans << nline;
}

